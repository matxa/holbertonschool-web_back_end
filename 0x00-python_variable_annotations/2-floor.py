#!/usr/bin/env python3
"""Write a type-annotated function floor
which takes a float n as argument and
returns the floor of the float.
"""


def floor(n: float) -> int:
    """Get the floor of a float

    Parameters
    ----------
    n : float
        number to floor

    Returns
    -------
    int
        converted float to int
    """
    return int(n)
