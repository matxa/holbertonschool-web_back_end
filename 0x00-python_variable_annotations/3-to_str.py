#!/usr/bin/env python3
"""Write a type-annotated function to_str that
takes a float n as argument and returns the
string representation of the float.
"""


def to_str(n: float) -> str:
    """Convert float to string

    Parameters
    ----------
    n : float
        number to convert to string

    Returns
    -------
    str
        converted float to string
    """
    return str(n)
