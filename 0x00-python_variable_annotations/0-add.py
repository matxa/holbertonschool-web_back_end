#!/usr/bin/env python3
"""Write a type-annotated function add
that takes a float a and a float b as arguments
and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """Add two floats

    Parameters
    ----------
    a : float
        first number
    b : float
        second number

    Returns
    -------
    float
        the sum of a + b
    """
    return a + b
